package Codigo;
public class Punto {
    private int x;
    private int y;
    //Getters
    public void setX(int x){
        this.x=x;
    }
    public void setY(int y){
        this.y=y;
    }
    //Setters
    public int getX(){
        return this.x;
    }
    public int getY(){
        return this.y;
    }

}
